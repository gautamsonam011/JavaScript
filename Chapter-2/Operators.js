// 1:- Arithmetic Operators!!---------> 

a = 34;
b = 5;

// Addition (+)
console.log("sum",a+b);

// Subtraction (-)
console.log("Sub",a-b);

// Multiplication(*)
console.log("Mul",a*b);

// Exponentiation(**)
console.log("Exp",a**b);
// Division(/)
console.log("Div",a/b);

// Modulus(%)
console.log("Modulus",a%b);

// Increment (++)
console.log("Inc",a++);
console.log("Inc",a++);

// Decrement (--)
console.log("Dec",a--);
console.log("Dec",a--);


