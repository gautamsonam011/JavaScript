// 2:- Assignment Operators!!------> 

x = 4;
y = 3;
// Assign (=)
console.log(x);
console.log(x+=y);
console.log(x-=y);
console.log(x*=y);
console.log(x/=y);
console.log(x%=y);
console.log(x**=y)

// 3:- Comparison Operators !! -----------------> 
// ==	equal to
let comp1 = 6;
let comp2 = 3;
console.log("comp1==comp2",comp1==comp2);
// === equal value and equal type
console.log("comp1===comp2",comp1===comp2);
// != not equal
console.log("comp1!=comp2",comp1!=comp2);
// !== not equal value or not equal type
console.log("comp1!==comp2",comp1!==comp2);
// > greater than
console.log("comp1>comp2",comp1>comp2);
// < less than
console.log("comp1<comp2",comp1<comp2);
// >= greater than or equal to
console.log("comp1>=comp2",comp1>=comp2);
// <= less than or equal to
console.log("comp1<=comp2",comp1<=comp2);
// ? ternary operator
console.log("comp1==comp2",comp1==comp2 ? "True":"False");
marks = 45;
console.log((marks>10) ? "Yes":"No");

